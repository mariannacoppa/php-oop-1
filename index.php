<?php
    //inizializzo la classe Movie nella quale dichiaro 3 variabili
    class Movie {
        public $title;
        public $genre;
        public $year;

        //definisco un costruttore
        function __construct($title, $genre, $year) {
            $this->title = $_title;
            $this->genre = $_genre;
            $this->year = $_year;
        }
    };
?>